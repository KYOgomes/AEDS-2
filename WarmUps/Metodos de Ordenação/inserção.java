//metodo de ordenação inserção 
import java.util.Scanner;
public class inserção {
    
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int n = sc.nextInt();

        //vetor criado
        int[] vetor = new int[n];
        //preencher vetor
        for (int i = 0; i < n; i++) 
        {
            vetor[i] = sc.nextInt();
        }


        
    }
}
